package day13;

public class MoreStringPractice {
    public static void main(String[] args) {
        String name = "Ankar";
        System.out.println(name);
        name = "Bulent";
        System.out.println(name);
//Strings are immutable.
        System.out.println(name.toUpperCase());
        System.out.println(name);
    }
}
