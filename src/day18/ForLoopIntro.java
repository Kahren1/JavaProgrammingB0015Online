package day18;

public class ForLoopIntro {
   /* (initialization;termination condition;increment)
   initialization: initializes the loop, it is executed just once, when the loop initializes
   termination condition:
   increment
    */
   public static void main(String[] args) {
       //count 1 to 5

       for(int i = 1;i<5;i++) {
           System.out.println("i = " + i);

           System.out.println("Happy birthday, Roxana!");
       }
       for (int counter=10;counter>0; ){
           System.out.println(counter);
           counter--;
       }

       //for (int i = 0; i < ; i++) {
           //awesome shortcut fori
           

   }
}
